SELECT 
  * 
FROM 
  VW_AUDIT_RM_ORDENS_VENDA FAT
WHERE 
  FAT.COD_ESTABELECIMENTO IN ('R351', 'R352')
  AND DATA_NOTA_FISCAL BETWEEN '2025-07-01' AND '2025-12-31'
  AND CFOP IN ('1.202', '1.411', '1.661', '1.949', '2.202', '5.916')
  AND (
    NUM_NOTA_FISCAL LIKE '%EST%' 
    OR NUM_NOTA_FISCAL LIKE '%NFD%'
  )
