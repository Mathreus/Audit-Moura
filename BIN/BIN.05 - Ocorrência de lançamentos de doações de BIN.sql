SELECT 
    COD_ESTABELECIMENTO,
    COD_FORNECEDOR,
    NOME_FORNECEDOR,
    DATA,
    DEPOSITO,
    COD_ITEM,
    DESC_ITEM,
    NUM_NOTA_FISCAL,
    QTD,
    PESO,
    FAMILIA_COMERCIAL,
    CUSTO_FINANCEIRO,
    TIPO_OPERACAO,
    DESC_TIPO_OPERACAO
FROM
    VW_AUDIT_RM_GERENCIAMENTO_ESTOQUE
WHERE
    COD_ESTABELECIMENTO IN ('R351', 'R352') -- ALTERAR PARA O DISTRIBUIDOR QUE IRÁ SER AUDITADO!!!
    AND DATA BETWEEN '2025-07-01' AND '2025-12-31' -- ALTERAR PARA O ESCOPO A SER AUDITADO!!!
    AND FAMILIA_COMERCIAL = 'BIN'
    AND TIPO_OPERACAO IN ('1.7.002', '1.2.038') -- CASO EXISTA MAIS ALGUM TIPO DE OPERAÇÃO DE "DOAÇÕES DE BIN", PODE INSERIR!!!
