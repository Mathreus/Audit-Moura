SELECT  
    COD_ESTABELECIMENTO,
    DEPOSITO,
    DATA,
    NUM_NOTA_FISCAL,
    SUM(QTD) AS QUANTIDADE,
    SUM(CUSTO_FINANCEIRO) AS VALOR,
    TIPO_OPERACAO,
    DESC_TIPO_OPERACAO
FROM    
    VW_AUDIT_RM_GERENCIAMENTO_ESTOQUE
WHERE
    COD_ESTABELECIMENTO = 'R351'
    AND DATA BETWEEN '2025-07-01' AND '2025-12-31'
    AND TIPO_OPERACAO IN ('1.7.001', '2.7.001') -- Tipos de operação com cliente sem financeiro
GROUP BY
    COD_ESTABELECIMENTO,
    DEPOSITO,
    DATA,
    NUM_NOTA_FISCAL,
    TIPO_OPERACAO,
    DESC_TIPO_OPERACAO
ORDER BY
    DATA DESC,
    NUM_NOTA_FISCAL;
